var $ = $ || jQuery;
$(function(){
	var w = $(window).width(),
	    toggle 		= $('#toggle-menu'),
	    menu 		= $('nav ul'),
	    hasChild = $('.has-child'),
	    dropdown = $('.dropdown');
	
	$(toggle).on('click', function(e) {
		e.preventDefault();
		menu.toggle();
	});

	$(hasChild).click(function(e) {
		// e.preventDefault();
		dropdown.toggle();
